# arithmetic
a = 4
b = 2

output = a + b
output = a - b
output = a * b
output = a / b
output = a % b
output = a ** b # exponent
output = a // b # floor division


# comparison
4 == 2 # False, is equal to
4 != 2 # True, is not equal to
4 > 2 # True, greater than
4 < 2 # False, lesser than
4 >= 2 # True, greater than or equal to
4 <= 2 # False, lesser than or equal to

# assignment
a = 2 # basic
a += 2 # gets the current value of variable a, then add 2, and assign
a -= 2
a *= 2
a /= 2

# performs operator from both sides
# and assigns the value on the left or a = a % 2
a %= 2
a **= 2
a //= 2


# logical operators
a = True
b = False

output = (a and b)
output = (a or b)
output = not(True) # False


# membership operators
l = [1, 2, 3, 4]
output = 2 in l
output = 2 not in l
